#include <stdio.h>

int main (void)
{
    char c = 'C';
    short s = 20;
    int i = 30;
    long l = 40;
    float f = 10.05;
    double d = 20.50;
    long double ld = 10.3054;
    printf("Data Types: \n");
    printf("Char: %c\nShort: %i\nInt: %i\nLong: %li\nFloat: %f\nDouble: %lf\nLong Double: %Lf\n", c, s, i, l, f, d, ld);

}